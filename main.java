public class Main {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        System.out.println("덧셈: " + (a + b));
        System.out.println("뺄셈: " + (a - b));
        System.out.println("곱셈: " + (a * b));  // 추가
        System.out.println("나눗셈: " + (a / b)); // 추가
    }
}
